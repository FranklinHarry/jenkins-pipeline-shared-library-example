/**
 * Jenkins Script Console — export installed plugins as a Gradle version catalog.
 *
 * Run this in Manage Jenkins → Script Console on the Jenkins instance whose plugin
 * versions you want to pin in your shared library's test dependencies.
 *
 * The output is a valid `jenkins.versions.toml` file.  Save it to
 * `gradle/jenkins.versions.toml` in your shared library project and wire it in
 * `settings.gradle.kts`:
 *
 *   dependencyResolutionManagement {
 *     versionCatalogs {
 *       create("jenkinsPlugins") {
 *         from(files("gradle/jenkins.versions.toml"))
 *       }
 *     }
 *   }
 *
 * Then reference plugins by their short name as typed accessors:
 *
 *   jenkinsPlugin(jenkinsPlugins.workflow.job)
 *   jenkinsPlugin(jenkinsPlugins.pipeline.model.definition)
 *
 * Re-run and replace the file whenever plugins are upgraded on the instance.
 */

import hudson.PluginWrapper
import java.time.Instant

// Convert a plugin short name (kebab-case) to a TOML key (dots for nesting, safe characters).
// "workflow-job" → "workflow.job", "pipeline-model-api" → "pipeline.model.api"
String tomlKey(String shortName) {
  shortName.replaceAll('-', '.')
}

final List<Map<String, String>> allPlugins = Jenkins.instance.pluginManager.plugins
  .findAll { PluginWrapper p -> p.active && !p.deleted }
  .collect { PluginWrapper p ->
    [
      groupId: p.manifest.mainAttributes.getValue('Group-Id') ?: 'unknown',
      shortName: p.shortName,
      version: p.version,
    ]
  }
  .sort { Map<String, String> p -> p.shortName }

// Plugins without a Group-Id in their manifest cannot produce a valid module coordinate.
// Print them as warnings and exclude them from the catalog.
final List<Map<String, String>> unknownPlugins = allPlugins.findAll { Map<String, String> p -> p.groupId == 'unknown' }
if (unknownPlugins) {
  unknownPlugins.each { Map<String, String> p ->
    System.err.println("WARNING: no Group-Id in manifest for ${p.shortName} — add manually if needed")
  }
}

final List<Map<String, String>> plugins = allPlugins.findAll { Map<String, String> p -> p.groupId != 'unknown' }

final String versions = plugins
  .collect { Map<String, String> p -> "${tomlKey(p.shortName)} = \"${p.version}\"" }
  .join('\n')

final String libraries = plugins
  .collect { Map<String, String> p ->
    "${tomlKey(p.shortName)} = { module = \"${p.groupId}:${p.shortName}\", version.ref = \"${tomlKey(p.shortName)}\" }"
  }
  .join('\n')

final String bundleEntries = plugins
  .collect { Map<String, String> p -> "    \"${tomlKey(p.shortName)}\"" }
  .join(',\n')

print """\
# Generated by scripts/export-jenkins-catalog.groovy
# Source: ${Jenkins.instance.rootUrl ?: 'unknown'}
# Jenkins: ${Jenkins.VERSION}
# Date: ${Instant.now()}

[versions]
${versions}

[libraries]
${libraries}

[bundles]
all-plugins = [
${bundleEntries},
]
"""
